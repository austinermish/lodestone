/**
 * Regressions for the Batch B (3.1.0) server-security fixes:
 *
 *  2.4  decodeAndValidateVaultId must reject `/`, malformed percent-encoding,
 *       and anything outside the plugin's actual vaultId alphabet.
 *  2.10 inferUpdateProvider must use exact-host matching, not substring —
 *       "fakegithub.com" and "github.evil.com" must NOT be treated as github.
 *
 * Run: node --import jiti/register tests/batch-b-security-regressions.mjs
 */
const vaultIdModule = await import("../server/src/vaultId.ts");
const { decodeAndValidateVaultId } = vaultIdModule.default ?? vaultIdModule;

let passed = 0;
let failed = 0;

function assert(condition, name) {
	if (condition) {
		console.log(`  PASS  ${name}`);
		passed++;
	} else {
		console.error(`  FAIL  ${name}`);
		failed++;
	}
}

console.log("\n--- 2.4: decodeAndValidateVaultId rejects malicious/malformed vaultIds ---");
{
	// Valid: real vaultIds generated by randomBase64Url(16) are 22 chars from
	// [A-Za-z0-9_-]; test infra vaultIds (e.g. "lodestone-integration-abc123") match too.
	assert(decodeAndValidateVaultId("AbCdEf01-_23456789012") === "AbCdEf01-_23456789012", "valid base64url vaultId accepted");
	assert(decodeAndValidateVaultId("lodestone-test-vault-1") === "lodestone-test-vault-1", "valid hyphenated test vaultId accepted");

	// Path traversal / R2 key injection.
	assert(decodeAndValidateVaultId("abc%2Fdef%2Fghijkl") === null, "encoded slash is rejected");
	assert(decodeAndValidateVaultId("abc/def/ghijkl") === null, "raw slash is rejected");
	assert(decodeAndValidateVaultId("../../etc/passwd") === null, "path traversal string is rejected");

	// Malformed percent-encoding must not throw past this function.
	let threw = false;
	let result;
	try {
		result = decodeAndValidateVaultId("%E0%A4%A");
	} catch {
		threw = true;
	}
	assert(!threw, "malformed percent-encoding does not throw");
	assert(result === null, "malformed percent-encoding is rejected (not thrown)");

	// Length bounds.
	assert(decodeAndValidateVaultId("short1") === null, "below 8-char minimum is rejected");
	assert(decodeAndValidateVaultId("x".repeat(65)) === null, "above 64-char maximum is rejected");
	assert(decodeAndValidateVaultId("x".repeat(64)) === "x".repeat(64), "exactly 64 chars is accepted");

	// Disallowed characters (would previously reach R2 key construction raw).
	assert(decodeAndValidateVaultId("abc def ghij") === null, "spaces are rejected");
	assert(decodeAndValidateVaultId("abc<script>ghij") === null, "angle brackets are rejected");
}

console.log("\n--- 2.10: inferUpdateProvider host-matching (source inspection) ---");
{
	// inferUpdateProvider is a private plugin method requiring a full Obsidian
	// App context to instantiate — following this repo's existing convention
	// (see editor-binding-health-regressions.mjs) of verifying fixed logic via
	// source inspection when the method can't be isolated cheaply.
	const { readFileSync } = await import("node:fs");
	const mainSource = readFileSync(new URL("../src/main.ts", import.meta.url), "utf8");
	const start = mainSource.indexOf("private inferUpdateProvider(");
	const end = mainSource.indexOf("\n\t}", start);
	const section = mainSource.slice(start, end);

	assert(section.length > 0, "inferUpdateProvider method found");
	assert(!section.includes('includes("github.")'), "substring match on 'github.' removed (matched fakegithub.com)");
	assert(!section.includes('includes("gitlab.")'), "substring match on 'gitlab.' removed (matched fakegitlab.com)");
	assert(section.includes('host === "github.com"'), "exact match on github.com present");
	assert(section.includes('host === "gitlab.com"'), "exact match on gitlab.com present");
	assert(section.includes('parsed.protocol !== "https:"'), "non-https URLs are rejected");

	// Confirm the exact vulnerable pattern from the pre-fix code cannot regress
	// via a differently-shaped substring check either.
	assert(!/host\.includes\(["']github["']\)/.test(section), "no bare 'github' substring check reintroduced");
	assert(!/host\.includes\(["']gitlab["']\)/.test(section), "no bare 'gitlab' substring check reintroduced");
}

console.log("\n──────────────────────────────────────────────────");
console.log(`Results: ${passed} passed, ${failed} failed`);
console.log("──────────────────────────────────────────────────");
process.exit(failed > 0 ? 1 : 0);
